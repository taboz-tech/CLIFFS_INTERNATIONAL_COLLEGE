   /**
    * 
    * @param type $gradeId
    * @param type $gradeName
    * @param type $gradeTeacher_id
    */
   public function edit($gradeId, $gradeName, $gradeTeacher_id){
       $data = ['name'=>$gradeName, 'teacher_id'=>$gradeTeacher_id];
       
       $this->db->where('id', $gradeId);
       $this->db->update('grades', $data);
       
       return TRUE;
   }
   
