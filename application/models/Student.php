    /**
     * 
     * @param type $value
     * @return boolean
     */
    public function studentsearch($value){
        $q = "SELECT * FROM students 
            WHERE 
            name LIKE '%".$this->db->escape_like_str($value)."%'
            || 
            student_id LIKE '%".$this->db->escape_like_str($value)."%'";
        
        $run_q = $this->db->query($q, [$value, $value]);
        
        if($run_q->num_rows() > 0){
            return $run_q->result();
        }
        
        else{
            return FALSE;
        }
    }
    
    
